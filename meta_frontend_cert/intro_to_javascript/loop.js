//Practice for and while loops

//count 1 to 5

for (i = 1; i < 6; i++) {
    console.log(i)
}
console.log("Counting completed!")

//count down from 5 to 1 

for (i = 5; i > 0; i--) {
    console.log(i)
}
console.log("Countdown finished!")

var i = 1

while (i < 6) {

    console.log(i)
    i += 1 
}
console.log("Countdown completed!")

var i = 5

while (i > 0) {
    console.log(i)
    i -= 1
}
console.log("Countdown complete!")